    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "insert into daftarbuku values (?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, dbbuku.getText());
            stat.setString(2, dbnama.getText());
            stat.setString(3, dbpengarang.getText());
            stat.setString(4, dbpenerbit.getText());
            stat.setString(5, dbterbit.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            kosong();
            dbbuku.requestFocus();
            datatable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan" +e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void dbtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dbtableMouseClicked
        // TODO add your handling code here:
        int bar = dbtable.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();
        
        dbbuku.setText(a);
        dbnama.setText(b);
        dbpengarang.setText(c);
        
        dbpenerbit.setText(d);
        dbterbit.setText(e);
    }//GEN-LAST:event_dbtableMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "update daftarbuku set nama_buku=?,pengarang=?,penerbit=?,terbit=? where kode_buku=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, dbnama.getText());
            stat.setString(2, dbpenerbit.getText());
            stat.setString(3, dbpengarang.getText());
            stat.setString(4,dbterbit.getText());
            stat.setString(5,dbbuku.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            kosong();
            dbbuku.requestFocus();
            datatable();
            
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah" +e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

