    protected void datatable() {
        Object[] Baris ={"Kode Buku","Nama Buku","Penerbit","Pengarang","Tahun Terbit"};
        tabmode = new DefaultTableModel(null, Baris);
        dbtable.setModel(tabmode);
        String sql="SELECT * FROM daftarbuku";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("kode_buku");
                String b = hasil.getString("nama_buku");
                String c = hasil.getString("pengarang");
                String d = hasil.getString("penerbit");
                String e = hasil.getString("terbit");
                
                String[] data={a,b,c,d,e};
                tabmode.addRow(data);
        }
        }  catch (SQLException e) {
        }
    }
