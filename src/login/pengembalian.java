     public void hitung_denda(){
        try {
            int bayar_denda = 500;
            String total;
            DateFormat tgl = new SimpleDateFormat("dd-MM-yyyy");
            String tanggal1 = ktpinjam.getText();
            Date tglp = (Date)tgl.parse(tanggal1);
            String tanggal2 = ktkembali.getText();
            Date tglk = (Date)tgl.parse(tanggal2);
            long dnd = Math.abs(tglp.getTime()-tglk.getTime());
            long telat = (TimeUnit.MILLISECONDS.toDays(dnd));
            if(telat >=5){
            kdenda.setText(String.valueOf((telat-5)*bayar_denda));
            }else{
            kdenda.setText(String.valueOf(telat*0));
            }        
             } catch (ParseException ex) {}
        }
    
    
    
    protected void datatable() {
        Object[] Baris ={"Kode Pinjam","Nama Buku","Tanggal Pinjam","Tanggal Kembali","Keterangan","Denda",};
        tabmode = new DefaultTableModel(null, Baris);
        tpengembalian.setModel(tabmode);
        String sql="SELECT * FROM kembali";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("kode_pinjam");
                String b = hasil.getString("nama_buku");
                String c = hasil.getString("tgl_pinjam");
                String d = hasil.getString("tgl_kembali");
                String e = hasil.getString("keterangan");
                String f = hasil.getString("denda");
                
                String[] data={a,b,c,d,e,f};
                tabmode.addRow(data);
        }
        }  catch (SQLException e) {
        }
    }

