    private void ksimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ksimpanActionPerformed
        // TODO add your handling code here:
        String sql = "insert into kembali values (?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, kpinjam.getText());
            stat.setString(2, kbuku.getText());
            stat.setString(3, ktpinjam.getText());
            stat.setString(4, ktkembali.getText());
            stat.setString(5, kketerangan.getSelectedItem().toString());
            stat.setString(6, kdenda.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            kosong();
            kpinjam.requestFocus();
            datatable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan" +e);
        }
    }//GEN-LAST:event_ksimpanActionPerformed

